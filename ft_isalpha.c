int	ft_isalpha(int c)
{
	int islower;
	int isupper;

	islower = (c >= 'a' && c <= 'z') ? 1 : 0;
	isupper = (c >= 'A' && c <= 'Z') ? 1 : 0;
	return (islower || isupper ? 1 : 0);
}
