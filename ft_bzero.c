#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		idx;
	char		*str;

	idx = 0;
	str = (char *)s;
	while (idx < n)
	{
		*(str + idx) = 0;
		idx++;
	}
}
